                if type(base64Chunk) ~= 'string' or base64Chunk == '' then
                    cb({ ok = false, reason = 'missing_chunk_data' })
                    return
                end
                TriggerServerEvent('SonoranCAD::bodycam::UploadRecordingChunk', uploadId, chunkIndex, base64Chunk)
                cb({ ok = true })
            end)

