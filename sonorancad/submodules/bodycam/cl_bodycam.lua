            RegisterNUICallback('bodycamStreamState', function(data, cb)
                local ready = data and data.ready == true
                peerStreamReady = ready
                debugLog(('Bodycam stream state changed: ready=%s reason=%s active=%s displayOn=%s'):format(
                    tostring(ready),
                    tostring(data and data.reason or 'unknown'),
                    tostring(peerStreamActive),
                    tostring(bodyCamDisplayOn)
                ))
                if ready then
                    FlushPendingRecordingStart('stream_state_ready')
                elseif not ready and pendingRecordingStart and data and data.reason and data.reason ~= 'stopped' then
                    debugLog(('Bodycam stream not ready for pending recording start: reason=%s'):format(tostring(data.reason)))
                end
                cb({ ok = true })
