local function nowMs()
    return GetGameTimer()
end

local function normalizeTriggerName(triggerName)
    if not triggerName then
        return nil
    end
    local normalized = tostring(triggerName):lower()
    normalized = normalized:gsub("%s+", "_")
    return normalized
end

local function isRecordingAutoTriggerEnabled(triggerName)
    local normalized = normalizeTriggerName(triggerName)
    if not normalized or type(recordingConfig.autoRecordEvents) ~= "table" then
        return false
    end
    for _, configured in ipairs(recordingConfig.autoRecordEvents) do
        if normalizeTriggerName(configured) == normalized then
            return true
        end
    end
    return false
end

local function logRecordingEvent(level, eventName, sourceType, triggerName, reason)
    TriggerServerEvent("SonoranCAD::bodycam::LogRecordingEvent", {
        level = level or "info",
        event = eventName,
        sourceType = sourceType,
        trigger = triggerName,
        reason = reason
    })
end

local function summarizeUploadPayload(data)
    if type(data) ~= "table" then
        return "payload=invalid"
    end
    local fileData = data.fileData
    local fileDataLength = type(fileData) == "string" and #fileData or 0
    return ('fileName=%s source=%s trigger=%s durationMs=%s size=%s fileDataChars=%s stopReason=%s'):format(
        tostring(data.fileName or 'nil'),
        tostring(data.sourceType or 'unknown'),
        tostring(data.trigger or 'none'),
        tostring(data.durationMs or '0'),
        tostring(data.size or '0'),
        tostring(fileDataLength),
        tostring(data.stopReason or 'unknown')
    )
end

