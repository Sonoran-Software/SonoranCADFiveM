                cb({ ok = true })
            end)

            RegisterNUICallback('bodycamRecordingState', function(data, cb)
                local state = data and data.state or nil
                if state == 'started' then
                    recordingActive = true
                    logRecordingEvent('debug', 'recording_started_ui', data.sourceType, data.trigger,
                        ('shadow=%sms live=%sms total=%sms'):format(
                            tonumber(data.shadowDurationMs) or 0,
                            tonumber(data.liveDurationMs) or 0,
                            tonumber(data.totalDurationMs) or 0
                        ))
                    if recordingConfig.enableRecordingSfx ~= false then
                        SendNUIMessage({
                            type = 'playSound',
                            transactionFile = recordingConfig.recordingStartSfx or 'sounds/beeps.mp3',
                            transactionVolume = soundLevel
                        })
                    end
                    TriggerEvent('chat:addMessage', {
                        args = { 'Sonoran Bodycam', 'Recording started.' }
                    })
                elseif state == 'stopped' then
                    recordingActive = false
                    local durationMs = tonumber(data.durationMs) or 0
                    local clipSize = tonumber(data.size) or 0
                    local stopReason = tostring(data.reason or 'unknown')
                    debugLog(('Bodycam recording ended: source=%s trigger=%s reason=%s durationMs=%s size=%s'):format(
                        tostring(data.sourceType or 'unknown'),
                        tostring(data.trigger or 'none'),
                        stopReason,
                        durationMs,
                        clipSize
                    ))
                    if durationMs < 1 or clipSize < 1 then
                        logRecordingEvent('warn', 'recording_stop_invalid', data.sourceType, data.trigger,
                            ('reason=%s durationMs=%s size=%s'):format(stopReason, durationMs, clipSize))
                    else
                        logRecordingEvent('debug', 'recording_stopped_ui', data.sourceType, data.trigger,
                            ('reason=%s durationMs=%s size=%s'):format(stopReason, durationMs, clipSize))
                    end
                    if recordingConfig.enableRecordingSfx ~= false then
                        SendNUIMessage({
                            type = 'playSound',
                            transactionFile = recordingConfig.recordingStopSfx or 'sounds/beep_off.mp3',
                            transactionVolume = soundLevel
                        })
                    end
                    TriggerEvent('chat:addMessage', {
                        args = { 'Sonoran Bodycam', 'Recording stopped. Uploading clip...' }
                    })
                elseif state == 'uploading' then
                    logRecordingEvent('debug', 'recording_uploading_ui', data.sourceType, data.trigger,
                        ('reason=%s durationMs=%s size=%s'):format(
                            tostring(data.reason or 'unknown'),
                            tostring(data.durationMs or '0'),
                            tostring(data.size or '0')
                        ))
                elseif state == 'failed' then
                    recordingActive = false
                    debugLog(('Bodycam recording failed: source=%s trigger=%s reason=%s durationMs=%s size=%s uploadStatus=%s uploadResponse=%s'):format(
                        tostring(data.sourceType or 'unknown'),
                        tostring(data.trigger or 'none'),
                        tostring(data.reason or 'unknown'),
                        tostring(data.durationMs or '0'),
                        tostring(data.size or '0'),
                        tostring(data.uploadStatus or 'nil'),
                        tostring(data.uploadResponse or '')
                    ))
                    TriggerEvent('chat:addMessage', {
                        args = { 'Sonoran Bodycam', ('Recording failed: %s'):format(tostring(data.reason or 'unknown')) }
                    })
                    logRecordingEvent('warn', 'recording_failed', data.sourceType, data.trigger,
                        ('reason=%s uploadStatus=%s uploadResponse=%s'):format(
                            tostring(data.reason or 'unknown'),
                            tostring(data.uploadStatus or 'nil'),
                            tostring(data.uploadResponse or '')
                        ))
                end
                cb({ ok = true })
            end)

